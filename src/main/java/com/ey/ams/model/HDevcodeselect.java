package com.ey.ams.model;

public class HDevcodeselect extends HObject {
    private String agkey;
    private String acname;
    private String acsubname;
    private String devcode;
    private String accode;
    private String acsubcode;
    private String taxcr;
    private String keycode;
    
    public String getAgkey() {
        return agkey;
    }
    public void setAgkey(String agkey) {
        this.agkey = agkey;
    }
    public String getAcname() {
        return acname;
    }
    public void setAcname(String acname) {
        this.acname = acname;
    }
    public String getAcsubname() {
        return acsubname;
    }
    public void setAcsubname(String acsubname) {
        this.acsubname = acsubname;
    }
    public String getDevcode() {
        return devcode;
    }
    public void setDevcode(String devcode) {
        this.devcode = devcode;
    }
    public String getAccode() {
        return accode;
    }
    public void setAccode(String accode) {
        this.accode = accode;
    }
    public String getAcsubcode() {
        return acsubcode;
    }
    public void setAcsubcode(String acsubcode) {
        this.acsubcode = acsubcode;
    }
    public String getTaxcr() {
        return taxcr;
    }
    public void setTaxcr(String taxcr) {
        this.taxcr = taxcr;
    }
    public String getKeycode() {
        return keycode;
    }
    public void setKeycode(String keycode) {
        this.keycode = keycode;
    }
    
}
